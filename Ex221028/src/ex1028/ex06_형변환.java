package ex1028;

public class ex06_형변환 {

	public static void main(String[] args) {
		
		// 자동 형변환, 묵시적 형변환
		int num = 5; 
		System.out.println(num);
		
		// 강제 형변환, 명시적 형변환
		double num2 = (double)num;
		System.out.println(num2);

		double num3 = 5.6;
		System.out.println(num3);
		
		int num4 = (int)num3;
		System.out.println(num4);
		
		
		
	}

}
